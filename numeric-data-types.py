# Coba operasi matematika
# + - / * %
print("=== Math Operation ===")
print(2 + 2)
print(2 - 2)
print(2 / 2)
print(2 * 2)
print(9 % 2) # Modulus: Sisa bagi


# Variable: Pythin merupakan jenis dynamic typing nih boss
print("=== Variable ===")
myVariable = 1
print(myVariable)
print(type(myVariable))

# Variable overriding
myVariable = "Zauvik"
print(myVariable)
print(type(myVariable))


myVariable2 = "Rizaldi"
print(myVariable2)

# Menyambungkan string
print(myVariable + " " + myVariable2)

myVariableConnect = myVariable + " " + myVariable2
print(myVariableConnect)
print(type(myVariableConnect))


# Variable data type conversion
myVariable3 = 19
print(myVariable3)
print(type(myVariable3))
print(myVariableConnect + str(myVariable3))
print(type(str(myVariable3))) # Cek myVariable3 setelah proses conversion


